dia_da_semana = int(input("Digite um número de 1 a 7: "))
if dia_da_semana == 1:
    print("Domingo")
elif dia_da_semana == 2:
    print("Segunda-feira")
elif dia_da_semana == 3:
    print("Terça-feira")
elif dia_da_semana == 4:
    print("Quarta-feira")
elif dia_da_semana == 5:
    print("Quinta-feira")
elif dia_da_semana == 6:
    print("Sexta-feira")
elif dia_da_semana == 7:
    print("Sábado")
else:
    print("Número inválido. Por favor, digite um número de 1 a 7.")